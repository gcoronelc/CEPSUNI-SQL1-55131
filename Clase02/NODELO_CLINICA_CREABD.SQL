



-- =============================================
-- Creación de la Base de Datos
-- =============================================

USE master;
go

IF( EXISTS ( SELECT name FROM master.sys.databases WHERE name = 'CLINICA' ) )
BEGIN
	DROP DATABASE CLINICA;
END;
go

CREATE DATABASE CLINICA;
go


-- =============================================
-- Creció la Base de Datos
-- =============================================

use clinica;
go





CREATE TABLE EMPLEADO
( 
	IDEMPLEADO           integer IDENTITY ( 1,1 ) ,
	NOMBRE               varchar(100)  NOT NULL ,
	APELLIDO             varchar(100)  NOT NULL ,
	CORREO               varchar(20)  NOT NULL ,
	DNI                  varchar(20)  NOT NULL ,
	TELEFONO             varchar(20)  NOT NULL ,
	CONSTRAINT XPKEMPLEADO PRIMARY KEY (IDEMPLEADO ASC)
)
go



CREATE TABLE CONSULTORIO
( 
	IDCONSULTORIO        integer  NOT NULL ,
	NOMBRE               varchar(20)  NOT NULL ,
	PISO                 integer  NOT NULL ,
	NUMERO               integer  NOT NULL ,
	CONSTRAINT XPKCONSULTORIO PRIMARY KEY (IDCONSULTORIO ASC)
)
go



CREATE TABLE ESPECIALIDAD
( 
	IDESPACIALIDAD       integer  NOT NULL ,
	NOMBRE               varchar(100)  NOT NULL ,
	CAPACIDAD            integer  NOT NULL ,
	CONSTRAINT XPKESPECIALIDAD PRIMARY KEY (IDESPACIALIDAD ASC)
)
go



CREATE TABLE MEDICO
( 
	IDMEDICO             integer IDENTITY ( 1,1 ) ,
	IDESPACIALIDAD       integer  NOT NULL ,
	NOMBRE               varchar(100)  NOT NULL ,
	APELLIDO             varchar(100)  NOT NULL ,
	DNI                  varchar(20)  NOT NULL ,
	TELEFONO             varchar(20)  NOT NULL ,
	CORREO               varchar(20)  NOT NULL ,
	CONSTRAINT XPKMEDICO PRIMARY KEY (IDMEDICO ASC),
	CONSTRAINT FK_MEDICO_ESPECIALIDAD FOREIGN KEY (IDESPACIALIDAD) REFERENCES ESPECIALIDAD(IDESPACIALIDAD)
		ON DELETE NO ACTION
		ON UPDATE NO ACTION
)
go



CREATE TABLE TURNO
( 
	IDTURNO              char(18)  NOT NULL ,
	NOMBRE               varchar(50)  NOT NULL ,
	INICIA               varchar(20)  NOT NULL ,
	FINALIZA             varchar(20)  NOT NULL ,
	CONSTRAINT XPKTURNO PRIMARY KEY (IDTURNO ASC)
)
go



CREATE TABLE PROGRAMACION
( 
	IDPROGRAMACION       integer IDENTITY ( 1,1 ) ,
	IDEMPLEADO           integer  NOT NULL ,
	IDCONSULTORIO        integer  NOT NULL ,
	IDMEDICO             integer  NOT NULL ,
	IDTURNO              char(18)  NOT NULL ,
	FECHA                datetime  NOT NULL ,
	CAPACIDAD            integer  NOT NULL ,
	CITAS                integer  NOT NULL ,
	PRECIO               integer  NOT NULL ,
	CONSTRAINT XPKPROGRAMACION PRIMARY KEY (IDPROGRAMACION ASC),
	CONSTRAINT FK_PROGRAMACION_CONSULTORIO FOREIGN KEY (IDCONSULTORIO) REFERENCES CONSULTORIO(IDCONSULTORIO)
		ON DELETE NO ACTION
		ON UPDATE NO ACTION,
CONSTRAINT FK_PROGRAMACION_MEDICO FOREIGN KEY (IDMEDICO) REFERENCES MEDICO(IDMEDICO)
		ON DELETE NO ACTION
		ON UPDATE NO ACTION,
CONSTRAINT FK_PROGRAMACION_TURNO FOREIGN KEY (IDTURNO) REFERENCES TURNO(IDTURNO)
		ON DELETE NO ACTION
		ON UPDATE NO ACTION,
CONSTRAINT FK_PROGRAMACION_EMPLEADO FOREIGN KEY (IDEMPLEADO) REFERENCES EMPLEADO(IDEMPLEADO)
		ON DELETE NO ACTION
		ON UPDATE NO ACTION
)
go



CREATE TABLE PACIENTE
( 
	IDPACIENTE           integer IDENTITY ( 1,1 ) ,
	APELLIDO             varchar(100)  NOT NULL ,
	NOMBRE               varchar(100)  NOT NULL ,
	TELEFONO             varchar(20)  NOT NULL ,
	DNI                  varchar(20)  NOT NULL ,
	CORREO               varchar(20)  NOT NULL ,
	CONSTRAINT XPKPACIENTE PRIMARY KEY (IDPACIENTE ASC)
)
go



CREATE TABLE CITA
( 
	IDCITA               integer  NOT NULL ,
	FECHA                integer  NOT NULL ,
	ORDEN                integer  NOT NULL ,
	IDEMPLEADO           integer  NOT NULL ,
	IDPROGRAMACION       integer  NOT NULL ,
	IDPACIENTE           integer  NOT NULL ,
	CONSTRAINT XPKCITA PRIMARY KEY (IDCITA ASC),
	CONSTRAINT FK_CITA_EMPLEADO FOREIGN KEY (IDEMPLEADO) REFERENCES EMPLEADO(IDEMPLEADO)
		ON DELETE NO ACTION
		ON UPDATE NO ACTION,
CONSTRAINT FK_CITA_PROGRAMACION FOREIGN KEY (IDPROGRAMACION) REFERENCES PROGRAMACION(IDPROGRAMACION)
		ON DELETE NO ACTION
		ON UPDATE NO ACTION,
CONSTRAINT FK_CITA_PACIENTE FOREIGN KEY (IDPACIENTE) REFERENCES PACIENTE(IDPACIENTE)
		ON DELETE NO ACTION
		ON UPDATE NO ACTION
)
go


